let n = Number(prompt("Enter a number : ",""));

switch (n) {
  case n = 0:
    alert("Zero");
    break;
  case n = 1:
    alert("One");
    break;
  case n = 2:
    alert("Two");
    break;
  case n = 3:
    alert("Three");
    break;
  case n = 4:
    alert("Four");
    break;
  case n = 5:
    alert("Five");
    break;
  case n = 6:
    alert("Six");
    break;
  case n = 7:
    alert("Seven");
    break;
  case n = 8:
    alert("Eight");
    break;
  case n = 9:
    alert("Nine");
    break;
}
